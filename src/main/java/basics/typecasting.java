package basics;

public class typecasting {
    public static void main(String[] args) {

        char letter = 'M';
        int number = 2;
        float value = 2.8f;
        double assign = 57.99;
        int change=(int)assign;
        float alter=(float)number;
        char alternate=(char)assign;
        double name=(double)number;
        System.out.println("my number is:"+change);
        System.out.println("My value is:"+alter);
        System.out.println("My letter is:"+alternate);
        System.out.println("My int value is:"+name);



    }
}